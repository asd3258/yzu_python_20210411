# 得到偶數的次數
import random as r
n = 0
while n % 2 == 0:
    n = r.randint(1, 100)
    print(n)

# 隨機亂數
import random
n = random.randrange(0, 5)  # 0 <= 5 (0, 1, 2, 3, 4)
print(n)
n = random.randint(0, 5)   # 0 <= n <= 5 (0, 1, 2, 3, 4, 5)
print(n)
n = random.randint(0, 5)   # 0 <= n <= 5 (0, 1, 2, 3, 4, 5)
print(n)

